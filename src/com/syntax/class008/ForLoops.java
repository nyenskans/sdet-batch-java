package com.syntax.class008;

public class ForLoops {

    public static void main(String[] args) {


        //	FOR LOOP


        // Best to use when we know how many times we need to repeat the code

        /* SYNTAX OF THE LOOP
         *
         * for(initialization;condition;increment/decrement){
         *
         * block of code;
         *
         * }
         *
         */


        // Say good morning 5 times


        for (int i = 1; i <= 5; i++) {
            System.out.println("Good morning");
        }
		
		/*
		 	for(int i=1;i<=5;i--) {
			System.out.println("Good morning");
			}		
			This will be an infinite loop
		 */


        // int i we call counter variable


    }

}
