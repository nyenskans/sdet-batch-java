package com.syntax.class001;

public class PrintOrPrintln {

    public static void main(String[] args) {
        // see difference between println vs print

        System.out.print("Hello ");

        System.out.println("How are you ");

        System.out.println("I am learning coding");


        /*
        	Println prints the statement with line spacing between statements

        	Print only prints the statement without the line break
         */

        // Spaces only matter in between double quotes ""
    }

}
